
public class ItemInformation {

	private String masteryLevel;
	private String weapon;
	private String soul;
	private String ring;
	private String earring;
	private String necklace;
	private String bracelet;
	private String belt;
	private String pet;
	private String soulBadge;
	private String mysticBadge;

	public String getMasteryLevel() {
		return masteryLevel;
	}
	public void setMasteryLevel(String masteryLevel) {
		this.masteryLevel = masteryLevel;
	}
	public String getWeapon() {
		return weapon;
	}
	public void setWeapon(String weapon) {
		this.weapon = weapon;
	}
	public String getSoul() {
		return soul;
	}
	public void setSoul(String soul) {
		this.soul = soul;
	}
	public String getRing() {
		return ring;
	}
	public void setRing(String ring) {
		this.ring = ring;
	}
	public String getEarring() {
		return earring;
	}
	public void setEarring(String earring) {
		this.earring = earring;
	}
	public String getNecklace() {
		return necklace;
	}
	public void setNecklace(String necklace) {
		this.necklace = necklace;
	}
	public String getBracelet() {
		return bracelet;
	}
	public void setBracelet(String bracelet) {
		this.bracelet = bracelet;
	}
	public String getBelt() {
		return belt;
	}
	public void setBelt(String belt) {
		this.belt = belt;
	}
	public String getPet() {
		return pet;
	}
	public void setPet(String pet) {
		this.pet = pet;
	}
	public String getSoulBadge() {
		return soulBadge;
	}
	public void setSoulBadge(String soulBadge) {
		this.soulBadge = soulBadge;
	}
	public String getMysticBadge() {
		return mysticBadge;
	}
	public void setMysticBadge(String mysticBadge) {
		this.mysticBadge = mysticBadge;
	}
}
